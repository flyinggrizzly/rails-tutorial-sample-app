require 'test_helper'

class UsersHelperTest < ActionView::TestCase
  def setup
    @user = User.new(name: 'Test', email: 'foo@bar.org')
  end

  test 'gravatar_for should return viable image tag' do
    assert_equal gravatar_for(@user), '<img alt="Test" class="gravatar" src="https://secure.gravatar.com/avatar/24191827e60cdb49a3d17fb1befe951b" />'
  end
end
